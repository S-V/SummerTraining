	inline Float3 interpolate(
		const Float3 &v0, const Float3 &v1,
		float mu,
		Float3 &normal)
	{
		Float3 normal4 = v0 + (v1 - v0) * mu;
		normal.x = normal4.x;
		normal.y = normal4.y;
		normal.z = normal4.z;
		normal = Float3_Normalized(normal);
		return v0 + (v1 - v0) * mu;
	}

	struct SurfPoint : AVolume::Sample {
		Float3	position;
	};
	/*
	Linearly interpolate the position where an isosurface cuts
	an edge between two vertices, each with their own scalar value
	*/
	inline void interpolate(
		const SurfPoint& p1, const SurfPoint& p2,
		Float3 &position, Float3 &normal)
	{
		float isolevel = 0.0f;
		//if (fabs(isolevel-p1.density) < 0.00001)
		//	return(p1);
		//if (fabs(isolevel-p2.density) < 0.00001)
		//	return(p2);
		//if (fabs(p1.density-p2.density) < 0.00001)
		//	return(p1);
		float mu = (isolevel - p1.distance) / (p2.distance - p1.distance);
		position = p1.position + (p2.position - p1.position) * mu;
		normal = p1.normal + (p2.normal - p1.normal) * mu;
		normal = Float3_Normalized(normal);
	}

